$("#addFencing2").click(function () {
    mapmostUE.flyTo({
        "location": [120.71466827, 31.311656074, 849.92260742],
        "rotation": [0, -58.538494, -132.834899],
        "time": 2
    });

    // 注：目前只支持渲染features[0]这个多边形，数组中其它的多边形不渲染
    let data =
        {
            "type": "FeatureCollection",
            "name": "test1",
            "crs": {"type": "name", "properties": {"name": "urn:ogc:def:crs:OGC:1.3:CRS84"}},
            "features": [
                {
                    "type": "Feature", "properties": {"id": 1,"height":20}, "geometry": {
                        "type": "Polygon",
                        "coordinates": [

                            [
                                [120.698457,31.319447],
                                [120.713692,31.3076],
                                [120.723897,31.314572]
                            ]

                        ]
                    }
                }
            ]
        }

    let options = {
        "extrudeHeight": 100, // 单位米
        "extrudeColor": "#291fba",
        "groundColor": "#66b4cc"
    }

    mapmostUE.addFencing("Fencing1",data, options);
})

